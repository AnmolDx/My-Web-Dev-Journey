let a = 10;
let b = 5;

//Arithmatic Operators
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);

//Unary Operators
console.log(a++); //it only adds the value and store it
console.log(a);
console.log(++a); // it adds and show

//Assignment Operators
console.log(a+=b); //a = a + b
console.log(a-=b); //a = a - b
console.log(a*=b); //a = a * b
console.log(a/=b); //a = a / b
console.log(a%=b); //a = a % b

//Comparision Operators
console.log(a>b);  //Greater than
console.log(a>=b); //greater than equal to
console.log(a<b); //less than
console.log(a<=b); //less than equal to
console.log(a==b); //equal to (compares value, not type)
console.log(a==="b"); //equal to (compares both type and value)
console.log(a!=b); //not equal to