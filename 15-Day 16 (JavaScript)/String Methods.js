//trim method
let msg = "   hello world        ";
msg.trim()
console.log(msg)

//upper case method
let str = "Hello World";
console.log(str.toUpperCase())

//lower case method
console.log(str.toLowerCase())

//indexOf
console.log(str.indexOf("World")) //returns 6

//Slice method
let passion = "codingislove";
console.log(passion.slice(-4))

//replace & repeat method
console.log(passion.replace("love", "good"))
console.log(passion.repeat(5))